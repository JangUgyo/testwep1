create or replace function public.apply_stock_move(p_item_id bigint, p_kind text, p_qty numeric, p_reason text, p_warehouse_id bigint default null, p_subtype text default null)
returns public.inventory_items language plpgsql security definer set search_path = public as $$
declare it public.inventory_items; wid bigint; cur numeric; newqty numeric; total numeric;
begin
  if p_qty is null or p_qty <= 0 then raise exception 'qty must be positive'; end if;
  select * into it from public.inventory_items where id = p_item_id for update;
  if not found then raise exception 'item not found'; end if;
  if not (public.is_admin() or it.dept_id = public.my_dept()) then raise exception 'forbidden'; end if;
  wid := p_warehouse_id;
  if wid is null then select id into wid from public.warehouses order by id limit 1; end if;
  if wid is null then raise exception 'no warehouse'; end if;
  select qty into cur from public.inventory_stock where item_id = p_item_id and warehouse_id = wid for update;
  if not found then cur := 0; insert into public.inventory_stock(item_id, warehouse_id, qty) values (p_item_id, wid, 0); end if;
  if p_kind = 'in' then newqty := cur + p_qty;
  elsif p_kind = 'out' then newqty := cur - p_qty;
  else raise exception 'invalid kind'; end if;
  if newqty < 0 then raise exception 'insufficient stock'; end if;
  update public.inventory_stock set qty = newqty where item_id = p_item_id and warehouse_id = wid;
  select coalesce(sum(qty),0) into total from public.inventory_stock where item_id = p_item_id;
  update public.inventory_items set stock = total where id = p_item_id;
  insert into public.stock_moves(item_id, kind, qty, reason, subtype, actor, actor_id, dept_id, warehouse_id, balance_after)
    values (p_item_id, p_kind, p_qty, p_reason, p_subtype, (select name from public.profiles where id = auth.uid()), auth.uid(), it.dept_id, wid, newqty);
  select * into it from public.inventory_items where id = p_item_id;
  return it;
end$$;
grant execute on function public.apply_stock_move(bigint, text, numeric, text, bigint, text) to authenticated;

-- 창고 간 재고 이동 함수
create or replace function public.transfer_stock(p_item_id bigint, p_from_wh bigint, p_to_wh bigint, p_qty numeric, p_reason text)
returns public.inventory_items language plpgsql security definer set search_path = public as $$
declare it public.inventory_items; cur numeric; fromq numeric; toq numeric; fname text; tname text;
begin
  if p_qty is null or p_qty <= 0 then raise exception 'qty must be positive'; end if;
  if p_from_wh = p_to_wh then raise exception 'same warehouse'; end if;
  select * into it from public.inventory_items where id = p_item_id for update;
  if not found then raise exception 'item not found'; end if;
  if not (public.is_admin() or it.dept_id = public.my_dept()) then raise exception 'forbidden'; end if;
  select name into fname from public.warehouses where id = p_from_wh;
  select name into tname from public.warehouses where id = p_to_wh;
  select qty into cur from public.inventory_stock where item_id = p_item_id and warehouse_id = p_from_wh for update;
  if not found or cur < p_qty then raise exception 'insufficient stock'; end if;
  fromq := cur - p_qty;
  update public.inventory_stock set qty = fromq where item_id = p_item_id and warehouse_id = p_from_wh;
  if exists (select 1 from public.inventory_stock where item_id = p_item_id and warehouse_id = p_to_wh) then
    update public.inventory_stock set qty = qty + p_qty where item_id = p_item_id and warehouse_id = p_to_wh returning qty into toq;
  else
    insert into public.inventory_stock(item_id, warehouse_id, qty) values (p_item_id, p_to_wh, p_qty) returning qty into toq;
  end if;
  insert into public.stock_moves(item_id, kind, qty, reason, actor, actor_id, dept_id, warehouse_id, balance_after)
    values (p_item_id, 'out', p_qty, coalesce(p_reason,'창고 이동') || ' → ' || coalesce(tname,''), (select name from public.profiles where id = auth.uid()), auth.uid(), it.dept_id, p_from_wh, fromq);
  insert into public.stock_moves(item_id, kind, qty, reason, actor, actor_id, dept_id, warehouse_id, balance_after)
    values (p_item_id, 'in', p_qty, coalesce(p_reason,'창고 이동') || ' ← ' || coalesce(fname,''), (select name from public.profiles where id = auth.uid()), auth.uid(), it.dept_id, p_to_wh, toq);
  update public.inventory_items set stock = (select coalesce(sum(qty),0) from public.inventory_stock where item_id = p_item_id) where id = p_item_id;
  select * into it from public.inventory_items where id = p_item_id;
  return it;
end$$;
grant execute on function public.transfer_stock(bigint, bigint, bigint, numeric, text) to authenticated;


