create or replace function public.next_trade_no(p_kind text)
returns text language plpgsql security definer set search_path = public as $$
declare
  v_pre   text := case when lower(coalesce(p_kind,'po')) = 'po' then 'PO' else 'QT' end;
  v_scope text := v_pre || '-' || to_char(now(), 'YYYYMM');
  v_seed  int;
  v_next  int;
begin
  select coalesce(max(substring(doc_no from char_length(v_scope) + 2)::int), 0)
    into v_seed
    from public.trade_documents
   where doc_no ~ ('^' || v_scope || '-[0-9]+$');
  insert into public.doc_counters(scope, seq) values (v_scope, v_seed)
  on conflict (scope) do nothing;
  update public.doc_counters set seq = seq + 1 where scope = v_scope returning seq into v_next;
  return v_scope || '-' || lpad(v_next::text, 4, '0');
end; $$;
