-- ----------------------------------------------------------------------------
-- (7-2) 재고 입/출고 처리 함수 (창고·유형 인식형 최종본)
-- ----------------------------------------------------------------------------
-- 감사 로그용 컬럼/함수가 먼저 필요하므로 audit_row 정의 후 재정의합니다(아래 7-4).
create or replace function public.apply_stock_move(p_item_id bigint, p_kind text, p_qty numeric, p_reason text)
returns public.inventory_items
language plpgsql
security definer
set search_path = public
as $$
declare it public.inventory_items; newbal numeric;
begin
  if p_qty is null or p_qty <= 0 then raise exception 'qty must be positive'; end if;
  select * into it from public.inventory_items where id = p_item_id for update;
  if not found then raise exception 'item not found'; end if;
  if not (public.is_admin() or it.dept_id = public.my_dept()) then raise exception 'forbidden'; end if;
  if p_kind = 'in'  then newbal := coalesce(it.stock,0) + p_qty;
  elsif p_kind = 'out' then newbal := coalesce(it.stock,0) - p_qty;
  else raise exception 'invalid kind'; end if;
  if newbal < 0 then raise exception 'insufficient stock'; end if;
  update public.inventory_items set stock = newbal where id = p_item_id;
  insert into public.stock_moves(item_id, kind, qty, reason, actor, actor_id, dept_id, balance_after)
    values (p_item_id, p_kind, p_qty, p_reason,
            (select name from public.profiles where id = auth.uid()),
            auth.uid(), it.dept_id, newbal);
  select * into it from public.inventory_items where id = p_item_id;
  return it;
end$$;
grant execute on function public.apply_stock_move(bigint, text, numeric, text) to authenticated;


