create or replace function public.is_admin()
    returns boolean language sql stable security definer set search_path = public as $$
    select exists (select 1 from public.profiles where id = auth.uid() and role = 'admin');
$$;

create or replace function public.my_dept()
    returns text language sql stable security definer set search_path = public as $$
    select dept_id from public.profiles where id = auth.uid();
$$;

create or replace function public.is_approved()
    returns boolean language sql stable security definer set search_path = public as $$
    select exists (select 1 from public.profiles where id = auth.uid() and coalesce(approved,false) = true);
$$;

create or replace function public.handle_new_user()
    returns trigger language plpgsql security definer set search_path = public as $$
declare first_user boolean;
begin
    select count(*) = 0 into first_user from public.profiles;
    insert into public.profiles (id, email, name, position, dept_id, role, approved)
    values (
        new.id, new.email,
        coalesce(new.raw_user_meta_data->>'name', ''),
        coalesce(new.raw_user_meta_data->>'position', ''),
        coalesce(new.raw_user_meta_data->>'dept_id', 'strategy'),
        case when first_user then 'admin' else 'employee' end,
        case when first_user then true  else false      end
    );
    return new;
end; $$;

create or replace function public.protect_profile_fields()
    returns trigger language plpgsql security definer set search_path = public as $$
begin
    if not public.is_admin() then
        new.role     := old.role;
        new.approved := old.approved;
        new.dept_id  := old.dept_id;
    end if;
    return new;
end; $$;


