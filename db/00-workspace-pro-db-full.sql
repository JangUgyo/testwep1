create or replace function public.audit_row()
returns trigger language plpgsql security definer set search_path = public as $$
declare j jsonb; lbl text; det text;
begin
  if (TG_OP = 'DELETE') then j := to_jsonb(OLD); else j := to_jsonb(NEW); end if;
  lbl := coalesce(j->>'name', j->>'title', j->>'doc_no', j->>'sku', j->>'value', j->>'key', '#' || coalesce(j->>'id',''));
  det := coalesce(j->>'dept_id', '');
  insert into public.audit_logs(actor, actor_email, actor_id, action, target, detail, created_at)
  values (
